/**
 * @brief Check that a mesh has no duplicate triangles (same 3 vertex positions).
 * Duplicate triangles cause z-fighting artifacts.
 */
static int countDuplicateTriangles(const voxel::Mesh &mesh) {
	const voxel::IndexArray &indices = mesh.getIndexVector();
	const voxel::VertexArray &vertices = mesh.getVertexVector();
	const size_t numTris = indices.size() / 3;
	int duplicates = 0;

	for (size_t i = 0; i < numTris; ++i) {
		// Get sorted positions for triangle i
		glm::vec3 pi[3] = {
			vertices[indices[i * 3 + 0]].position,
			vertices[indices[i * 3 + 1]].position,
			vertices[indices[i * 3 + 2]].position
		};
		// Sort vertices for canonical comparison
		if (pi[0].x > pi[1].x || (pi[0].x == pi[1].x && (pi[0].y > pi[1].y || (pi[0].y == pi[1].y && pi[0].z > pi[1].z))))
			core::exchange(pi[0], pi[1]);
		if (pi[1].x > pi[2].x || (pi[1].x == pi[2].x && (pi[1].y > pi[2].y || (pi[1].y == pi[2].y && pi[1].z > pi[2].z))))
			core::exchange(pi[1], pi[2]);
		if (pi[0].x > pi[1].x || (pi[0].x == pi[1].x && (pi[0].y > pi[1].y || (pi[0].y == pi[1].y && pi[0].z > pi[1].z))))
			core::exchange(pi[0], pi[1]);

		for (size_t j = i + 1; j < numTris; ++j) {
			glm::vec3 pj[3] = {
				vertices[indices[j * 3 + 0]].position,
				vertices[indices[j * 3 + 1]].position,
				vertices[indices[j * 3 + 2]].position
			};
			if (pj[0].x > pj[1].x || (pj[0].x == pj[1].x && (pj[0].y > pj[1].y || (pj[0].y == pj[1].y && pj[0].z > pj[1].z))))
				core::exchange(pj[0], pj[1]);
			if (pj[1].x > pj[2].x || (pj[1].x == pj[2].x && (pj[1].y > pj[2].y || (pj[1].y == pj[2].y && pj[1].z > pj[2].z))))
				core::exchange(pj[1], pj[2]);
			if (pj[0].x > pj[1].x || (pj[0].x == pj[1].x && (pj[0].y > pj[1].y || (pj[0].y == pj[1].y && pj[0].z > pj[1].z))))
				core::exchange(pj[0], pj[1]);

			if (pi[0] == pj[0] && pi[1] == pj[1] && pi[2] == pj[2]) {
				++duplicates;
			}
		}
	}
	return duplicates;
}

// Helper to count duplicate triangles across all meshes in a MeshState for a given volume index
static int countAllDuplicateTriangles(const MeshState &meshState, int idx) {
	int total = 0;
	for (int type = 0; type < MeshType_Max; ++type) {
		const auto &meshesMap = meshState.meshes((MeshType)type);
		for (const auto &iter : meshesMap) {
			const MeshState::Meshes &meshes = iter->value;
			if (idx < (int)meshes.size() && meshes[idx] != nullptr) {
				total += countDuplicateTriangles(*meshes[idx]);
			}
		}
	}
	return total;
}

// Verify that extracting a single voxel at various positions never produces
// duplicate triangles (which cause z-fighting).
TEST_F(MeshStateTest, testCubicNoDuplicateTriangles_SingleVoxel) {
	// Test at position (32, 4, 48) which was reported to have artifacts
	const glm::ivec3 positions[] = {
		{32, 4, 48}, {0, 0, 0}, {15, 15, 15}, {61, 0, 0}, {0, 61, 0}, {0, 0, 61}
	};

	for (const auto &pos : positions) {
		voxel::RawVolume v(voxel::Region(0, 0, 0, 63, 63, 63));
		v.setVoxel(pos.x, pos.y, pos.z, voxel::createVoxel(voxel::VoxelType::Generic, 1));

		MeshState meshState;
		meshState.construct();
		core::Var::getVar(cfg::VoxelMeshMode)->setVal((int)voxel::SurfaceExtractionType::Cubic);
		meshState.init();
		bool deleted = false;
		palette::Palette pal;
		pal.nippon();
		(void)meshState.setVolume(0, &v, &pal, nullptr, true, deleted);

		meshState.scheduleRegionExtraction(0, v.region());
		meshState.extractAllPending();

		const int dups = countAllDuplicateTriangles(meshState, 0);
		EXPECT_EQ(dups, 0) << "Duplicate triangles at position (" << pos.x << "," << pos.y << "," << pos.z << ")";

		size_t vc = 0, nc = 0, ic = 0;
		meshState.count(MeshType_Opaque, 0, vc, nc, ic);
		EXPECT_EQ(ic, 36u) << "Single voxel should have 36 indices at (" << pos.x << "," << pos.y << "," << pos.z << ")";

		(void)meshState.shutdown();
	}
}

