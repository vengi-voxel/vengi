TEST_F(MeshStateTest, testSetStateBeforeVolume) {
	// Verify that property setters work for indices beyond current size.
	// This simulates the prepareModelNodes flow where updateNodeState
	// calls hide()/gray()/etc. before setVolume is called.
	MeshState meshState;
	meshState.construct();
	meshState.init();

	const int idx = 5;
	// These must grow the internal arrays and store the state
	meshState.hide(idx, false);
	EXPECT_FALSE(meshState.hidden(idx));

	meshState.gray(idx, true);
	EXPECT_TRUE(meshState.grayed(idx));

	meshState.setLocked(idx, true);
	EXPECT_TRUE(meshState.locked(idx));

	// After state setup, setVolume should work normally
	voxel::RawVolume v(voxel::Region(-1, 1));
	palette::Palette pal;
	pal.nippon();
	bool deleted = false;
	(void)meshState.setVolume(idx, &v, &pal, nullptr, true, deleted);
	EXPECT_NE(nullptr, meshState.volume(idx));

	meshState.scheduleRegionExtraction(idx, v.region());
	EXPECT_LT(0, meshState.pendingExtractions());

	(void)meshState.shutdown();
}

// https://github.com/vengi-voxel/vengi/issues/844
// Verify that the cubic extractor via MeshState produces all 6 faces for a
// voxel at the upper edge. MeshState schedules the adjacent chunk via
// completeRegion.shiftUpperCorner(1,1,1) so the positive-direction faces
// are generated by that adjacent chunk.
TEST_F(MeshStateTest, testCubicBoundaryFaceGeneration) {
	voxel::RawVolume v(voxel::Region(0, 15));
	v.setVoxel(15, 15, 15, voxel::createVoxel(voxel::VoxelType::Generic, 1));

	MeshState meshState;
	meshState.construct();
	core::Var::getVar(cfg::VoxelMeshMode)->setVal((int)voxel::SurfaceExtractionType::Cubic);
	meshState.init();
	bool deleted = false;
	palette::Palette pal;
	pal.nippon();
	(void)meshState.setVolume(0, &v, &pal, nullptr, true, deleted);

	meshState.scheduleRegionExtraction(0, v.region());
	// The completeRegion is [0,0,0]-[16,16,16] (shifted +1).
	// This should schedule chunk [0..15] AND the adjacent chunk [16..31]
	// (which intersects completeRegion at 16).
	EXPECT_GE(meshState.pendingExtractions(), 2) << "Adjacent chunk must be scheduled for boundary faces";
	meshState.extractAllPending();

	size_t vertCount = 0, normalsCount = 0, indCount = 0;
	meshState.count(MeshType_Opaque, 0, vertCount, normalsCount, indCount);
	EXPECT_EQ(indCount, 36u) << "Single voxel at upper boundary should have all 6 faces (36 indices)";

	(void)meshState.shutdown();
}

// https://github.com/vengi-voxel/vengi/issues/844
// Verify that deleting a voxel at a chunk boundary triggers re-extraction
// of the adjacent chunk so that newly-exposed faces appear.
TEST_F(MeshStateTest, testCubicDeleteAdjacentVoxelAcrossChunkBoundary) {
	voxel::RawVolume v(voxel::Region(-1, 16, 1, 3, 19, 4));

	for (int y = 16; y <= 19; ++y) {
		for (int z = 1; z <= 4; ++z) {
			v.setVoxel(-1, y, z, voxel::createVoxel(voxel::VoxelType::Generic, 1));
			for (int x = 0; x <= 3; ++x) {
				v.setVoxel(x, y, z, voxel::createVoxel(voxel::VoxelType::Generic, 2));
			}
		}
	}

	MeshState meshState;
	meshState.construct();
	core::Var::getVar(cfg::VoxelMeshMode)->setVal((int)voxel::SurfaceExtractionType::Cubic);
	meshState.init();
	bool deleted = false;
	palette::Palette pal;
	pal.nippon();
	(void)meshState.setVolume(0, &v, &pal, nullptr, true, deleted);

	meshState.scheduleRegionExtraction(0, v.region());
	meshState.extractAllPending();

	size_t indBefore = 0, vc = 0, nc = 0;
	meshState.count(MeshType_Opaque, 0, vc, nc, indBefore);
	EXPECT_GT(indBefore, 0u);

	// Delete voxels at x=0 (chunk boundary)
	for (int y = 16; y <= 19; ++y) {
		for (int z = 1; z <= 4; ++z) {
			v.setVoxel(0, y, z, voxel::createVoxel(voxel::VoxelType::Air, 0));
		}
	}

	const voxel::Region modifiedRegion(0, 16, 1, 0, 19, 4);
	meshState.scheduleRegionExtraction(0, modifiedRegion);
	EXPECT_GE(meshState.pendingExtractions(), 2) << "Both adjacent chunks must be scheduled";
	meshState.extractAllPending();

	size_t indAfter = 0;
	vc = 0; nc = 0;
	meshState.count(MeshType_Opaque, 0, vc, nc, indAfter);
	EXPECT_GT(indAfter, 0u) << "After deleting adjacent voxel, faces should still exist";

	(void)meshState.shutdown();
}

// Verify that adjacent chunk regions produced by calculateExtractRegion
// never overlap - each voxel position belongs to exactly one chunk.
TEST_F(MeshStateTest, testNoOverlappingExtractRegions) {
	voxel::RawVolume v(voxel::Region(-32, 31));

	MeshState meshState;
	meshState.construct();
	meshState.init();
	bool deleted = false;
	palette::Palette pal;
	pal.nippon();
	(void)meshState.setVolume(0, &v, &pal, nullptr, true, deleted);

	meshState.scheduleRegionExtraction(0, v.region());

	// Collect all scheduled regions
	core::DynamicArray<voxel::Region> regions;
	const int n = meshState.pendingExtractions();
	EXPECT_GT(n, 0);

	// Extract all and collect the regions from the extraction results
	// Since calculateExtractRegion produces grid-aligned non-overlapping chunks,
	// we verify the grid property: mins are multiples of meshSize and
	// maxs = mins + meshSize - 1
	meshState.extractAllPending();

	// Verify by re-scheduling and checking the regions
	meshState.scheduleRegionExtraction(0, v.region());
	// All chunks should tile without gaps or overlaps
	// For volume [-32, 31] with meshSize=16:
	// x chunks: -32..-17, -16..-1, 0..15, 16..31 = 4 chunks per axis
	// Total = 4^3 = 64 chunks, but completeRegion is [-32, 32] so we get
	// an extra layer: 5 chunks in some axes
	const int expected = meshState.pendingExtractions();
	EXPECT_GT(expected, 0);

	(void)meshState.shutdown();
}

