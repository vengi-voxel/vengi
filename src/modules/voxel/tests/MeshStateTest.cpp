// scheduleRegionExtraction must not touch a volume after it was cleared
// (SceneGraph may free the RawVolume once MeshState no longer holds the pointer).
TEST_F(MeshStateTest, testScheduleAfterVolumeCleared) {
	voxel::RawVolume *v = new voxel::RawVolume(voxel::Region(0, 0, 0, 7, 7, 7));
	v->setVoxel(0, 0, 0, voxel::createVoxel(voxel::VoxelType::Generic, 1));

	MeshState meshState;
	meshState.construct();
	meshState.init();
	bool deleted = false;
	palette::Palette pal;
	pal.nippon();
	(void)meshState.setVolume(0, v, &pal, nullptr, true, deleted);
	ASSERT_NE(nullptr, meshState.volume(0));

	meshState.scheduleRegionExtraction(0, v->region());
	(void)meshState.setVolume(0, nullptr, nullptr, nullptr, true, deleted);
	delete v;

	// Must not use-after-free
	EXPECT_FALSE(meshState.scheduleRegionExtraction(0, voxel::Region(0, 0, 0, 7, 7, 7)));
	(void)meshState.shutdown();
}

// Verify that the cubic mesher through MeshState does not produce
// overlapping geometry for a solid block that spans multiple chunks.
TEST_F(MeshStateTest, testCubicNoOverlappingFaces) {
	// 2x2x2 solid block spanning chunk boundary at x=0
	voxel::RawVolume v(voxel::Region(-1, -1, -1, 0, 0, 0));
	for (int x = -1; x <= 0; ++x)
		for (int y = -1; y <= 0; ++y)
			for (int z = -1; z <= 0; ++z)
				v.setVoxel(x, y, z, voxel::createVoxel(voxel::VoxelType::Generic, 1));

	MeshState meshState;
	meshState.construct();
	core::Var::getVar(cfg::VoxelMeshMode)->setVal((int)voxel::SurfaceExtractionType::Cubic);
	meshState.init();
	bool deleted = false;
	palette::Palette pal;
	pal.nippon();
	(void)meshState.setVolume(0, &v, &pal, nullptr, true, deleted);

	meshState.scheduleRegionExtraction(0, v.region());
	meshState.extractAllPending();

	size_t vertCount = 0, normalsCount = 0, indCount = 0;
	meshState.count(MeshType_Opaque, 0, vertCount, normalsCount, indCount);

	// A 2x2x2 cube has 6 faces, each 2x2 = 4 quads = 8 triangles per face.
	// Without merging: 6 * 4 * 2 * 3 = 144 indices.
	// With merging (within each chunk): each face may merge to fewer quads.
	// The key check: no MORE than 144 indices (no duplicates from overlapping chunks).
	EXPECT_GT(indCount, 0u);
	EXPECT_LE(indCount, 144u) << "2x2x2 cube must not have more than 144 indices (no overlapping)";

	(void)meshState.shutdown();
}

